# Load necessary libraries
library(ggplot2)       # Used for data visualization (scatter plots, histograms, etc.)
library(caret)         # Provides functions for machine learning workflows (data preprocessing, model training, evaluation)
library(rpart)         # Used for creating decision tree models (Recursive Partitioning)
library(rpart.plot)    # Used for visualizing decision trees generated by rpart
library(gridExtra)     # Helps arrange multiple ggplot2 plots in a grid layout
library(dplyr)        # Provides functions for data manipulation (filter, mutate, select, group_by, etc.)
library(reshape2)      # Used for reshaping data (converting between wide and long formats)
library(xgboost)       # Implements the XGBoost algorithm, an efficient and scalable gradient boosting framework
library(randomForest)  # Used for building Random Forest models (ensemble learning method)
library(scales)        # Provides functions for formatting scales in ggplot2 (e.g., formatting large numbers, percentages)
